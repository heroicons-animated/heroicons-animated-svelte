    animation: hashtag-draw 0.6s linear forwards;
  }

  @keyframes hashtag-draw {
    0% {
      stroke-dashoffset: 1;
      opacity: 0;
    }
    100% {
      stroke-dashoffset: 0;
      opacity: 1;
    }
  }
</style>
